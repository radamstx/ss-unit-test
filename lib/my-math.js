"use strict";

class MyMath {
  // add 2 numbers and return result
  add(a, b) {
    return a + b;
  }
  // subtract 2 numbers and return result
  subtract(a, b) {
    return a - b;
  }
  // multiply 2 numbers and return result
  multiply(a, b) {
    return a * b;
  }
  // divide 2 numbers and return result
  divide(a, b) {
    return a / b;
  }
}

module.exports = MyMath;